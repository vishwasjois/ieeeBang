        <div class="clearfix"><h2 id="concept">Conceptualization of GINI</h2></div>
        <article class="clearfix">
          Need: Over the years there was a large increase in SBs in a section especially in R10 and India in particular. This created a problem unique of its sorts where a SB was not able to reach across other SB to talk about its activities or to collaborate. Industry also missed out on reaching the entire student community due to this problem. 

          Solution: A network structure was envisaged to bridge the short comings by having multiple hubs/zones in a section. A leadership position was created at each of these hubs to coordinate/lead/take responsibility of the events/initiatives which are directed towards the student community from section/region/IEEE HQ level. Industry also preferred the same as it provided them a structure to bank upon for any initiatives driven by them. 

          Execution: A coordinator at the section level interfaces with the section SAC and the hub leaders in a section. He is also in contact with other GINI section coordinators from across R10. Hub leaders are the interface between the SB and the section SAC. A GINI coordinator in a SB will be the point of contact for a hub leader. 

          An online collaboration platform is used as a single platform to share/convey the information. More emphasis is given to this. Also relevant links to a SB execom can be accessed from this platform. An email list is used for communication of higher frequency. Typically the execom members and the GINI coordinator of a SB is registered for this email list. This is developed/maintained by the CP lead.

          A communication/Industry relation lead will coordinate the initiatives which are driven by the industry. He is the interface between the industry reps and hub leaders/SB reps.
